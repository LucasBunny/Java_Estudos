class tipos_primitivos {
    public static void main(String[] args) {
        byte byte_exemplo = 1;
        short short_exemplo = -100;
        int int_exemplo = 12;
        long long_exemplo = 1564543454235789L;
        
        float float_exemplo = 99.9f;
        double double_exemplo = 99.99999999;
        
        char char_exemplo = 'L';
        String string_exemplo = "Lucas";
        
        boolean bool_exemplo = true;

        
        System.out.println("Tipos Primitivos \u263A \n");
        System.out.println("Byte: " +byte_exemplo);
        System.out.println("Short: " +short_exemplo);
        System.out.println("Int: " +int_exemplo);
        System.out.println("Long: " +long_exemplo);
        
        System.out.println("Float: " +float_exemplo);
        System.out.println("Double: " +double_exemplo);
        
        System.out.println("Char: " +char_exemplo);
        System.out.println("String: " +string_exemplo);
        
        System.out.println("Boolean: " +bool_exemplo);

    }
}